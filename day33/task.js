// 1. Define React in your own words
// React is a JavaScript library used to build user interfaces, especially dynamic and interactive web applications. It helps developers create reusable components that update efficiently when data changes.

// 2. List 3 advantages of React
// - Reusable Components: Build once, reuse across the app.
// - Virtual DOM: Efficient updates without reloading the whole page.
// - Strong Ecosystem: Backed by Facebook and a huge developer community with tools and libraries.

// 3. Explain SPA (Single Page Application)
// A SPA is a web application that loads a single HTML page and dynamically updates content as the user interacts, without refreshing the entire page.
// - Example: Gmail or Facebook.
// - Benefit: Faster navigation and smoother user experience

// 4. Difference between React and JavaScript

//React                                            | Javascript

// A library built on JavaScript for building UIs  | A programming language

// Focuses on components and state management      | General-purpose, used for logic, DOM manipulation, etc.

// Provides Virtual DOM for performance            | Directly manipulates the real DOM

// 5. What is Virtual DOM?
// The Virtual DOM is a lightweight copy of the real DOM. React uses it to compare changes (diffing) and update only the necessary parts of the real DOM, making apps faster and more efficient

